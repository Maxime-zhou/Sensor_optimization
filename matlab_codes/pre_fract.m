a=1.d0;                                              % crack semilength
rad=0.02;                                            % radius or rosette

for i=1:analysis.NN,
 dist=sqrt(nodes(i).coor(2)^2+(nodes(i).coor(1)-a)^2); % distance of node from x_B
 if dist<.6*rad   
  nodes(i).coor(2)=.5*nodes(i).coor(2);               % modifies x_1
  nodes(i).coor(1)=a+.5*(nodes(i).coor(1)-a);         % modifies x_2
 end 
end

