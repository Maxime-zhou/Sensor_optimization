
%!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!
% Lumped mass matrix for T3
%!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!
function Me=T3_2A_solid_Me(X,mate)

rho=mate(3);
x11=X(1,1); x21=X(2,1); x31=X(3,1);           % nodal coordinates
x12=X(1,2); x22=X(2,2); x32=X(3,2);
S=.5*((x21-x11)*(x32-x12)-...
	                 (x31-x11)*(x22-x12));    % element area
Me=1/3*S*rho*ones(6,1);  

